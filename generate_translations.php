// Fonction pour générer un fichier de traduction
function generateTranslationFile($lang, $file, $translations) {
    $dir = __DIR__ . "/lang/{$lang}";
    if (!is_dir($dir)) {
        mkdir($dir, 0755, true);
    }
    
    $content = "<?php\n\nreturn [\n";
    $content .= "    /*\n";
    $content .= "    |--------------------------------------------------------------------------\n";
    $content .= "    | " . ucfirst($file) . " Language Lines\n";
    $content .= "    |--------------------------------------------------------------------------\n";
    $content .= "    */\n\n";
    
    foreach ($translations as $key => $value) {
        $escapedValue = str_replace("'", "\\'", $value);
        $content .= "    '{$key}' => '{$escapedValue}',\n";
    }
    
    $content .= "];\n";
    
    file_put_contents("{$dir}/{$file}.php", $content);
    echo "✓ Créé: lang/{$lang}/{$file}.php\n";
}

// Générer les fichiers auth.php pour toutes les langues
echo "Génération des fichiers de traduction...\n\n";
foreach ($translations as $lang => $files) {
    foreach ($files as $file => $content) {
        generateTranslationFile($lang, $file, $content);
    }
}

echo "\n✅ Génération terminée!\n";
echo "Fichiers créés: " . (count($languages) * count($translations['en'])) . "\n";
